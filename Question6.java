package Week44;

public class Question6 {
	
// Create a java program that prints values from 1 through 20, using the do-while loop statement.
	public static void main(String[] args) {
	
		int i = 1;
		System.out.println("Printing values from 1-20 using do-while loop.");
		do {
			System.out.println(i);
			i++;
		}while ( i <= 20);

	}

}
